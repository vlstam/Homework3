package mypack;
/**
 * Напишете програма, която да показва абсолютната стойност на
 * числата: -147, 15, 0, 18, резултата да се отпечатват на един и
 * съши ред, с разделение табулация.
 */
public class Homework3 {
    public static void main(String[] args) {
        int a = Math.abs(-147);
        int b = Math.abs(15);
        int c = Math.abs(0);
        int d = Math.abs(18);
        System.out.print(a+"\t"+b+"\t"+c+"\t"+d);
        //За трета и четвърта задача се пробвах по два начина да принтирам резултата
    }
}
